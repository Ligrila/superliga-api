<h3>¿Qué es el torneo de amigos?</h3>
<p>Jugada Superliga te da la posibilidad de jugar con tus amigos en la app, desafiarlos y
descubrir quién sabe más de fútbol.</p>

<h3>¿Cómo se hace?</h3>
<p>Creás un torneo en la app e invitás a tus amigos a participar. También podés sumarte a un
torneo ya armado. Tenés la chance de abrir todos los torneos que quieras y competir entre
todos tus diferentes grupos de amigos.</p>

<i class="fa fa-handshake fa-5x" ></i>
<h3>¿Qué es el desafío?</h3>
<p>Podes transformar tu “torneo” en un “equipo” y desafiar a otros. Así, en lugar de competir
contra tus amigos, van a sumar todos juntos para vencer a sus rivales. Todas las trivias
suman puntos.</p>

<h3>¿Cómo se quién es el mejor jugador de un torneo o el mejor equipo?</h3>
<p>En tus torneos vas a poder seguir trivia a trivia y semana a semana quién es el jugador que
suma más puntos. También vas a tener las estadísticas de tus desafíos en cada evento.</p>

<i class="fa fa-trophy fa-5x" ></i>
<h3>¿Gano premios?</h3>
<p>Va a haber premios especiales para los equipos ganadores. Cuántos más partidos y trivias
jugáis más chances tenés de sumar puntos y ganar premios especiales. Estos premios se
suman a los que podés ganar individualmente en cada evento</p>

