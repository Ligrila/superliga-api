<h3>¿Cuáles son los premios?</h3>
<p>El ganador del primer puesto en la gran final se llevará dos pasajes para viajar por la Argentina (*). En tanto del segundo al quinto puesto recibirán la camiseta de su equipo de Superliga.</p>

<h3>¿Cómo sé si clasifiqué a la final?</h3>
<p>Al final de cada trivia vas a enterarte si fuiste el ganador y si sumás comodín. Además, la tabla de los clasificados será publicada en la app y en nuestras redes sociales.
</p>

<p class="mt-5"><small>(*) Los pasajes se limitan a los destinos operados por la empresa Fly Bondi.</small></p>

