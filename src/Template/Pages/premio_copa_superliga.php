<h3>Viajá a ver la Copa de la Superliga</h3>
<p>Jugada Superliga te lleva a ver la gran final de la Copa de la Superliga a disputarse el 2 de junio en la ciudad de Córdoba.
</p>

<h3>¿Cómo podes ganar?</h3>
<p>Es fácil. Desde el 30 de abril hasta el 26 de mayo, todos los ganadores de los partidos y trivias diarias lograrán clasificar para una gran final en la que dirimirá al ganador del gran premio. Ese evento decisivo será el lunes 27 de mayo. Atento, que cada trivia que ganas sumas un comodín que te servirá para desempatar en caso de igualdad de puntos en la final.</p>

<i class="fa fa-trophy fa-5x" ></i>
<h3>¿Cuál es el premio?</h3>
<p>El ganador de la gran final viajará, junto a un acompañante, a ver la final de la Copa de la Superliga. El premio incluye pasajes, estadía y entradas VIP para el gran evento.</p>

<h3>¿Cómo sé si clasifiqué a la final?</h3>
<p>Al final de cada trivia vas a enterarte si fuiste el ganador y si sumás comodín. Además, la tabla de los clasificados será publicada en la app y en nuestras redes sociales.
</p>

