<p><strong>¿De qué se trata y cómo se juega?</strong></p>

<p>Una vez que te bajaste la app vas a poder jugar las trivias en vivo y las históricas. En cada partido tendrás que contestar una serie de preguntas relacionadas con las incidencias del encuentro y demostrar cuánto sabes de tu equipo y de la Superliga. </p>

<p><strong>¿Puedo jugar en cualquier momento?</strong></p>

<p>Las trivias son en vivo. Podes jugarlas el fin de semana durante el horario de cada partido de la Superliga. También habrá trivias semanales que deben contestarse en vivo.  </p>

<p><strong>¿Cómo gano?</strong></p>

<p>A medida que vas contestando correctamente sumas puntos que luego podes canjearlos por increíbles productos. También competirás todas las fechas contra otros usuarios para ganar importantes premios.</p>

<p><strong>¿Cómo son las jugadas en vivo?</strong></p>

<p>Tendrás 4 opciones sobre jugadas que vas a poder anticipar y contestar sobre su desenlace. Estas son las opciones de respuesta y lo que abarcan cada una.</p>

<p>1)PENAL</p>
<p>Opciones </p>
<p>Gol!!!, Ataja el arquero, Desviado</p>

<p>GOL: </p>
<p>Si la pelota ingresa en el arco y el árbitro convalida el gol.  </p>
<p>Si el arquero toca la pelota, pero ingresa al arco.</p>
<p>Si el arquero toca la pelota, pega en los palos o travesaño e ingresa al arco sin que otro jugador más la toque.</p>
<p>Si la pelota ingresa al arco y el árbitro anula el gol y hace patear de vuelta. En este caso se considera para la respuesta la segunda ejecución.</p>
<p>Si el ejecutante le pasa la pelota a un compañero y convierte.</p>

<p>ATAJA EL ARQUERO</p>
<p>Si el arquero agarra la pelota tras el disparo del ejecutante.</p>
<p>Si el arquero toca la pelota y la desvia. </p>
<p>Si el arquero toca la pelota y pega en los postes o travesaño y no ingresa.</p>
<p>Si el arquero desvía la pelota y cualquier atacante o compañero (de segunda jugada) anota gol.</p>

<p>Desviado</p>
<p>Si la pelota pega en los palos.</p>
<p>Si la pelota pega en el travesaño.</p>
<p>Si la pelota se va fuera del campo de juego.</p>
<p>Si la pelota se va a los costados y la toma un rival para hacer una segunda jugada (salvo la definida en punto uno).</p>
<p>Si la pelota pega en los palos o travesaño y la toma un atacante o defensor y es gol (segunda jugada), igual se considera desviado.</p>
<p>Si el árbitro considera que el penal fue mal ejecutado por culpa del delantero y lo da por errado.</p>


<p>2)TIRO LIBRE</p>
<p>Opciones  </p>
<p>Gol!!!, Ataja el arquero, No pasa nada</p>

<p>GOL</p>
<p>Si la pelota ingresa en el arco y el árbitro lo convalida.  </p>
<p>Si el arquero toca la pelota, pero ingresa al arco.</p>
<p>Si el arquero toca la pelota, pega en los palos o travesaño e ingresa al arco.</p>
<p>Si la pelota ingresa al arco y el árbitro anula el gol y hace patear de vuelta, se considera para la respuesta la segunda ejecución.</p>
<p>Si el ejecutante le pasa la pelota a un compañero y convierte.</p>
<p>Si la pelota ingresa luego que la toque un atacante o defensor.</p>
<p>Si la pelota ingresa al arco luego de una serie de pases provenientes del tiro libre.</p>


<p>ATAJA EL ARQUERO</p>
<p>Si el arquero agarra la pelota tras el disparo del ejecutante.</p>
<p>Si el arquero toca la pelota y la desvia. </p>
<p>Si el arquero toca la pelota y pega en los postes o travesaño y no ingresa.</p>
<p>Si el arquero desvía la pelota y cualquier atacante o compañero (de segunda jugada) anota gol.</p>
<p>Si la pasa a un compañero y luego la ataja el arquero.</p>

<p>NO PASA NADA</p>
<p>Si la pelota se va desviada sin que la toque nadie.</p>
<p>Si la pelota la recupera un rival.</p>
<p>Si el ejecutante le pasa la pelota a un rival.</p>
<p>Si la pelota pega en los postes o travesaño, sin que la toque el arquero, y se desvía.</p>
<p>Si la pelota pega en los postes o travesaño, sin que la toque el arquero, y luego un atacante anote el gol de segunda jugada.</p>
<p>Si durante la jugada el árbitro cobra penal o vuelve a cobrar tiro libre.</p>

<p>3) CORNER</p>
<p>Opciones</p>
<p>Gol, Ataja el arquero, No pasa nada</p>

<p>GOL</p>
<p>Si la pelota ingresa en el arco y el árbitro lo convalida.  </p>
<p>Si el ejecutante le pasa la pelota a un compañero y convierte.</p>
<p>Si es olímpico (gol directo).</p>
<p>Si el arquero toca la pelota, pero ingresa al arco.</p>
<p>Si el arquero toca la pelota, pega en los palos o travesaño e ingresa al arco.</p>
<p>Si la pelota ingresa al arco y el árbitro anula el gol y hace patear de vuelta, se considera para la respuesta la segunda ejecución.</p>
<p>Si la pelota ingresa luego que la toque casualmente o con intención un atacante o defensor.</p>
<p>Si el ejecutante le pasa la pelota a un compañero y este sigue la jugada y deriva en gol.</p>

<p>ATAJA EL ARQUERO</p>
<p>Si el arquero agarra la pelota tras el disparo del ejecutante.</p>
<p>Si el arquero toca la pelota y la desvia. </p>
<p>Si el arquero toca la pelota y pega en los postes o travesaño y no ingresa.</p>
<p>Si el arquero desvía la pelota y cualquier atacante o compañero (de segunda jugada) anota gol.</p>

<p>NO PASA NADA</p>
<p>Se considera si la pelota se va desviada sin que la toque nadie.</p>
<p>Si la pelota la recupera un rival.</p>
<p>Si la pelota es desviada por un defensor o atacante.</p>
<p>Si la pelota pega en los postes o travesaño, sin que la toque el arquero, y se desvía.</p>
<p>Si durante la jugada el árbitro cobra penal o tiro libre.</p>

<p> </p>
<p>4)SAQUE DE ARCO</p>
<p>Opciones</p>
<p>Pase al compañero, Pase al contrario, La tira afuera.</p>

<p>PASE AL COMPAÑERO</p>
<p>Si el arquero o defensor sacan y la pelota llega a un compañero.</p>
<p>Si el arquero o defensor sacan y la pelota rebota en un jugador pero llega a un compañero.</p>
<p>Si el arquero o defensor sacan y es gol de arco a arco.</p>

<p>PASE AL CONTRARIO</p>
<p>Si el arquero o defensor sacan y la pelota llega a un contrario.</p>
<p>Si el arquero o defensor sacan y la pelota rebota en un jugador pero llega a un contrario. </p>
<p>Si el arquero o defensor sacan y el árbitro cobra fuera de juego y la posesión pasa al contrario.</p>

<p>LA TIRA AFUERA</p>
<p>Si el arquero o defensor sacan y la pelota se va de la cancha.</p>
<p>Si el arquero o defensor sacan y la pelota rebota en un jugador y se va de la cancha.</p>



